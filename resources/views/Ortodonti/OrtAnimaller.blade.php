                        </dd>   <dd class="col-sm-9 offset-sm-3 mt-4">  -İskeletsel sınıf II anomali: Bu anomalide alt çene kafa kaidesine göre geride yada üst çene kafa kaidesine göre önde olmakla birlikte iki durumun kombinasyonu şeklinde de olabilir. Bireylerde konveks profil mevcuttur.

